public class Task9 {
    public static void main(String[] args) {
        int binary = 0b1010;
        int octal = 012;
        int hex = 0xA;

        System.out.println("0b1010 (binary) = " + binary);
        System.out.println("012 (octal) = " + octal);
        System.out.println("0xA (hex) = " + hex);
    }
}
